#include "Hamming.h"
#include <algorithm>
#include <climits>
#include <iostream>


// ФУНКЦИЯ ВЫЧИСЛЕНИЯ ЧИСЕЛ ХЭММИНГА

// Алгоритм основан на использовании трех очередей для чисел, умноженных на 2, 3 и 5.
// Идея: каждое число Хэмминга (кроме первого) получается умножением предыдущего
// числа Хэмминга на 2, 3 или 5.


std::vector<unsigned long long> computeHammingNumbers(int n) {
    // Проверка корректности входных данных
    if (n <= 0) {
        throw InvalidInputException("Количество чисел должно быть положительным");
    }

    // При n > 90 числа становятся больше 2^64-1, что вызывает переполнение unsigned long long
    if (n > 90) {
        throw OverflowException("Для n > 90 требуется Big Integer (unsigned long long недостаточно)");
    }


    // ИНИЦИАЛИЗАЦИЯ ТРЕХ ОЧЕРЕДЕЙ

    // Создаем три очереди для хранения кандидатов:
    // - q2 хранит числа, которые в 2 раза больше уже напечатанных
    // - q3 хранит числа, которые в 3 раза больше уже напечатанных
    // - q5 хранит числа, которые в 5 раза больше уже напечатанных


    Queue<unsigned long long> q2, q3, q5;

    try {
        // Начальное заполнение: умножаем первое число (1) на 2, 3 и 5
        // 1 * 2 = 2, 1 * 3 = 3, 1 * 5 = 5
        q2.push(2);
        q3.push(3);
        q5.push(5);
    }
    catch (const QueueException& e) {
        std::cerr << "[WARNING] Ошибка инициализации очередей: " << e.what() << std::endl;
        q2.clear();
        q3.clear();
        q5.clear();

        // Восстановление: уменьшаем n и пробуем снова
        if (n > 50) {
            std::cerr << "[RECOVERY] Уменьшаем n до 50 и продолжаем" << std::endl;
            return computeHammingNumbers(50);
        }
        throw ComputationException(std::string("Невозможно инициализировать очереди: ") + e.what());
    }

    // Вектор для хранения результата
    std::vector<unsigned long long> result;
    result.reserve(n);  // Резервируем память для оптимизации


    // ОСНОВНОЙ ЦИКЛ АЛГОРИТМА

    // Алгоритм работает следующим образом:
    // 1. На каждом шаге смотрим на первые элементы трех очередей
    // 2. Выбираем наименьший из них - это следующее число Хэмминга
    // 3. Удаляем это число из всех очередей (если оно там есть)
    // 4. Добавляем в каждую очередь это число, умноженное на 2, 3 и 5
    // 5. Повторяем, пока не наберем n чисел


    while (result.size() < n) {


        // ШАГ 1: НАХОЖДЕНИЕ МИНИМАЛЬНОГО ЭЛЕМЕНТА

        // Смотрим на первые элементы каждой очереди.
        // Если очередь пуста, используем ULLONG_MAX (максимальное число),
        // чтобы она не влияла на выбор минимума.


        unsigned long long min2 = q2.empty() ? ULLONG_MAX : q2.front();
        unsigned long long min3 = q3.empty() ? ULLONG_MAX : q3.front();
        unsigned long long min5 = q5.empty() ? ULLONG_MAX : q5.front();

        // Выбираем наименьшее из трех чисел
        // Это и есть следующее число Хэмминга
        unsigned long long minVal = std::min({ min2, min3, min5 });


        // ШАГ 2: ПРОВЕРКА НА ПЕРЕПОЛНЕНИЕ

        // Проверяем, не превысит ли следующее умножение пределы unsigned long long
        // Если minVal > ULLONG_MAX / 5, то minVal * 5 вызовет переполнение


        if (minVal > ULLONG_MAX / 5) {
            std::cerr << "[WARNING] Достигнут предел unsigned long long при n="
                << result.size() + 1 << std::endl;
            if (result.empty()) {
                throw OverflowException("Невозможно вычислить даже первое число");
            }
            return result;  // Возвращаем частичный результат
        }


        // ШАГ 3: ДОБАВЛЕНИЕ ЧИСЛА В РЕЗУЛЬТАТ


        result.push_back(minVal);


        // ШАГ 4: УДАЛЕНИЕ ВСЕХ ВХОЖДЕНИЙ МИНИМАЛЬНОГО ЭЛЕМЕНТА

        // Важно: одно и то же число может находиться в нескольких очередях
        // Например, 6 = 2*3 и 6 = 3*2, поэтому оно будет и в q2, и в q3
        // Удаляем его из всех очередей, где оно есть


        if (!q2.empty() && q2.front() == minVal) q2.pop();
        if (!q3.empty() && q3.front() == minVal) q3.pop();
        if (!q5.empty() && q5.front() == minVal) q5.pop();


        // ШАГ 5: ДОБАВЛЕНИЕ КРАТНЫХ ЭЛЕМЕНТОВ

        // Умножаем текущее число на 2, 3 и 5 и добавляем в соответствующие очереди
        // Это создает кандидатов для следующих шагов алгоритма

        q2.push(minVal * 2);   // Добавляем число, умноженное на 2
        q3.push(minVal * 3);   // Добавляем число, умноженное на 3
        q5.push(minVal * 5);   // Добавляем число, умноженное на 5
    }


    return result;
}